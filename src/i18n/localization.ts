function readCookieLanguage(): Language | null {
  if (typeof document === 'undefined') return null;
  const chunks = document.cookie.split(';').map((part) => part.trim());
  const entry = chunks.find((part) => part.startsWith(`${COOKIE_KEY}=`));
  if (!entry) return null;
  const rawValue = decodeURIComponent(entry.slice(COOKIE_KEY.length + 1));
  return rawValue === 'ru' || rawValue === 'en' ? rawValue : null;
}

export function getLanguage(): Language {
  return readCookieLanguage() ?? DEFAULT_LANGUAGE;
}

export function setLanguage(language: Language): void {
  if (typeof document === 'undefined') return;
  document.cookie = `${COOKIE_KEY}=${encodeURIComponent(language)}; path=/; max-age=${ONE_YEAR_SECONDS}; samesite=lax`;
}

export function t(key: string, vars?: Record<string, string | number>): string {
  const dictionary = dictionaries[getLanguage()];
  const fallback = dictionaries[DEFAULT_LANGUAGE];
  const resolved = resolveKey(dictionary, key) ?? resolveKey(fallback, key) ?? key;
  if (typeof resolved !== 'string') return key;
  return applyVars(resolved, vars);
}

function resolveKey(dictionary: Dictionary, key: string): string | Dictionary | undefined {
  const parts = key.split('.');
  let current: string | Dictionary | undefined = dictionary;
  for (const part of parts) {
    if (!current || typeof current === 'string') return undefined;
    current = current[part];
  }
  return current;
}

function applyVars(template: string, vars?: Record<string, string | number>): string {
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, name: string) => String(vars[name] ?? `{${name}}`));
}
