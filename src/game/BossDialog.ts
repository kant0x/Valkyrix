// ─── Helpers ─────────────────────────────────────────────────────────────────

function rand<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

function classify(text: string): 'good' | 'neutral' | 'bad' {
  const words = text.trim().split(/\s+/).filter(Boolean).length;
  if (words <= 1) return 'bad';
  if (words <= 4) return 'neutral';
  return 'good';
}

// ─── Public API ───────────────────────────────────────────────────────────────

/** Returns a random boss opening line for when the overlay first mounts. */
export function getOpening(): string {
  return rand(OPENING);
}

/**
 * Classifies the player's message by word count and returns a scripted boss
 * reply with the corresponding outcome.
 */
export function getResponse(text: string): {
  reply: string;
  outcome: 'good' | 'neutral' | 'bad';
} {
  const outcome = classify(text);
  const pool = outcome === 'good' ? GOOD : outcome === 'neutral' ? NEUTRAL : BAD;
  return { reply: rand(pool), outcome };
}
